// Simple Arithmetic in TypeScript

// Addition
export const add = (num1: number, num2: number): number => num1 + num2;

// Subtraction
export const subtract = (num1: number, num2: number): number => num1 - num2;

// Division
export const divide = (num1: number, num2: number): number => num1 / num2;

// Multiplication
export const multiply = (num1: number, num2: number): number => num1 * num2;
